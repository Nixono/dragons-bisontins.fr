				<p class="lead text-justify">
					Sur le principe, le floorball est un cousin du hockey sur glace. L'énorme différence est l'absence de contact entre les joueurs. Il se pratique en gymnase, sur du parquet (sans rollers !). 5 joueurs ainsi qu'un gardien s'affrontent pour mettre le plus de buts possibles durant 3 tiers-temps de 20 minutes (pour les matchs de championnat). 
				</p>

